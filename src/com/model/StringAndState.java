package com.model;

public class StringAndState {

	private String name;
	private int state;

	public StringAndState() {
		super();
	}

	public StringAndState(String name, int state) {
		super();
		this.name = name;
		this.state = state;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getState() {
		return state;
	}

	public void setState(int state) {
		this.state = state;
	}

}
